#!/usr/bin/env node
/**
 * Generates the parts of the documentation that must never drift from the code.
 *
 * Sphinx' autodoc is a PYTHON tool - it cannot read JavaScript. So instead of
 * pretending, this script extracts the hard facts straight from the source and
 * writes them as Markdown into docs/_generated/. Those files are committed (so
 * Read the Docs needs no Node at build time) and verified in CI: if someone
 * changes a constant, an env var or a protocol message without regenerating,
 * `npm run docs:check` fails.
 *
 *   node scripts/gen-docs.js          # write docs/_generated/*.md
 *   node scripts/gen-docs.js --check  # fail if the committed files are stale
 */
const fs = require('fs');
const path = require('path');

const ROOT = path.join(__dirname, '..');
const OUT_DIR = path.join(ROOT, 'docs', '_generated');
const BANNER = '<!-- GENERATED by scripts/gen-docs.js - do not edit by hand. Run `npm run docs:gen`. -->\n\n';

const read = (p) => fs.readFileSync(path.join(ROOT, p), 'utf8');

