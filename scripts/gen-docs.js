function generateConfiguration() {
  const files = ['server.js', ...fs.readdirSync(path.join(ROOT, 'game')).filter((f) => f.endsWith('.js')).map((f) => `game/${f}`)];
  const found = new Map(); // name -> { default, files:Set }

  for (const file of files) {
    const src = read(file);
    // `Number(process.env.X) || 30000` — the wrapper hides the default from the
    // plain pattern below, so handle it first.
    const reNum = /Number\(process\.env\.([A-Z_][A-Z0-9_]*)\)\s*\|\|\s*([0-9_]+)/g;
    let m;
    while ((m = reNum.exec(src)) !== null) {
      const entry = found.get(m[1]) || { default: '', files: new Set() };
      entry.default = `\`${m[2]}\``;
      entry.files.add(file);
      found.set(m[1], entry);
    }
    // `process.env.X || path.join(a, b, 'c')` - the generic pattern below would
    // truncate at the first comma, so capture the whole call first.
    const reJoin = /process\.env\.([A-Z_][A-Z0-9_]*)\s*\|\|\s*path\.join\(([^)]*)\)/g;
    while ((m = reJoin.exec(src)) !== null) {
      const inner = m[2];
      let def;
      if (inner.includes("'models'")) def = '`models/` inside the image';
      else if (inner.includes("'data'")) def = '`<app>/data`';
      else def = '_(path relative to the app)_';
      const entry = found.get(m[1]) || { default: '', files: new Set() };
      entry.default = def;
      entry.files.add(file);
      found.set(m[1], entry);
    }
    const re = /process\.env\.([A-Z_][A-Z0-9_]*)\s*(?:\|\|\s*([^;,)\n]+?))?\s*(?:[;,)\n]|===)/g;
    while ((m = re.exec(src)) !== null) {
      const name = m[1];
      let def = (m[2] || '').trim();
      // Tidy up the extracted default expression
      if (def.startsWith('path.join')) {
        // The joined path tells us what it actually defaults to.
        if (def.includes("'models'")) def = '`models/` in the image';
        else if (def.includes("'data'")) def = '`<app>/data`';
        else def = '_(a path relative to the app)_';
      }
      else if (def.startsWith('`http://$')) def = 'the request\'s own host';
      else if (def === 'null') def = 'unset';
      else if (def) def = `\`${def.replace(/`/g, '')}\``;
      const entry = found.get(name) || { default: '', files: new Set() };
      if (def && !entry.default) entry.default = def;
      entry.files.add(file);
      found.set(name, entry);
    }
    // `=== '1'` style flags have no || default
    const reFlag = /process\.env\.([A-Z_][A-Z0-9_]*)\s*===\s*'([^']+)'/g;
    while ((m = reFlag.exec(src)) !== null) {
      const entry = found.get(m[1]) || { default: '', files: new Set() };
      if (!entry.default) entry.default = `unset (compare against \`${m[2]}\`)`;
      entry.files.add(file);
      found.set(m[1], entry);
    }
  }
  // Vars only referenced indirectly (docker-compose / secrets helper)
  for (const extra of ['PIKDAME_DATABASE_URL', 'PIKDAME_DATABASE_PASSWORD']) {
    if (!found.has(extra)) found.set(extra, { default: 'unset', files: new Set(['game/AccountStore.js']) });
  }

  const rows = [...found.entries()]
    .sort(([a], [b]) => a.localeCompare(b))
    .map(([name, e]) => {
      const note = ENV_NOTES[name] || '_(undocumented - see source)_';
      return `| \`${name}\` | ${e.default || '—'} | ${note} |`;
    });

  return `${BANNER}# Environment variables

All settings are environment variables - there is no config file. This table is
generated from the source, so it cannot go stale.

| Variable | Default | Meaning |
| --- | --- | --- |
${rows.join('\n')}

:::{note}
Secrets can also be supplied via a \`*_FILE\` variable pointing at a file (Docker
secrets), e.g. \`PIKDAME_DATABASE_PASSWORD_FILE=/run/secrets/db_password\`.
:::
`;
}

